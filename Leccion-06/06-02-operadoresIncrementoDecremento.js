/**
 * Operadores de incremento y decremento en JavaScript
 */

let a = 3, b = 2;
let z = a + b;
 console.log(z)

//Incremento 
//Pre-incremento (el operador  ++ antes de la variable)
z = ++a;
console.log(a);
console.log(z);

//Post-incremento (el operador ++ despues de la variable)

z = b++;
console.log(z);
console.log(b);

//Decremento

//Pre-decremente 

z = --a;
console.log(z)
console.log(a);

//Post-decremento

z = b--;
console.log(z);
console.log(b);