/**
 * Operadores Aritméticos en JavaScript
 */

//Operador de Adicción
let a = 3, b = 8;
let z = a + b;
console.log("El resultado de la suma es: " + z);
//Operador de Sustracción
z = a - b;
console.log("El resultado de la resta es: " + z);

//Operador de Multiplicació 
z = a * b;
console.log("El resultado de la multiplicació es: " + z);

//Operador de División
z = a / b;
console.log("El resultado de la división es: " + z);

//Operador Modulo
z = a % b;
console.log("El resultado del modulo/residuo de la división es: " + z);

//Operador de Exponente
z = a ** b;
console.log("El resultado de la potenciación es: " + z);
